      + (y[4] * 5.8798142449083754e-02) + (y[5] * 5.5508435061791990e-02)
      + (y[6] * 3.0296842402491128e-02) + (y[7] * 2.9399071224541877e-02)
      + (y[8] * 2.4983760555638834e-01) + ((*y_N) * 3.5697100110375433e-02);
